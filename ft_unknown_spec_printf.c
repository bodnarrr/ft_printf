#include "ft_printf.h"

int	ft_unknown_spec_printf(char c)
{
	if (c == 'i' || c == 's' || c == 'o' || c == 'x' || c == 'p' || c == 'd'
		|| c == 'O' || c == 'u' || c == 'U' || c == 'D' || c == 'X' || c == 'c'
		|| c == '%' || c == 'S' || c == 'C')
		return (0);
	return (1);
}
